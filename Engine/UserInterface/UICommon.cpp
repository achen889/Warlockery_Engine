#include "UICommon.hpp"


//===========================================================================================================

Clock* g_UIClock = NULL;

void AllocUIClock() {
	g_UIClock = new Clock("UIClock", &GetGameClock());
	GetGameClock().AddChild(g_UIClock);
}

//-----------------------------------------------------------------------------------------------------------

double GetUIDeltaSeconds() {
	if (g_UIClock) {
		return g_UIClock->GetDeltaSeconds();
	}
	return 0.0;
}

